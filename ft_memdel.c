#include "libft.h"

void	ft_memdel(void **ap)
{
	if (ap)
	{
		free(*ap);
		*ap = NULL;
	}
}

/*
** DESCRIPTION
** Memdel free the memory area sent as parameter and
** initiate the ptr to null.
*/
